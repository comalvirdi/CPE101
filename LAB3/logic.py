# Determines whether or not an int is even
# int --> bool
def is_even(num):

	return (num % 2 == 0)

#Determines whether or not a number falls within certain intervals
#float --> bool
def in_an_interval(num):

	return (-2 <= num < 9 or 22 < num < 42 or 12 < num <= 20 or 120 <= num <= 127)
